t_map *create_map_from_array(int rows, int columns, int input[rows][columns]) {
	t_map *map = malloc(sizeof(t_map));
	if (!map)
		return NULL;

	map->rows = rows;
	map->columns = columns;

	map->arr = malloc(sizeof(int *) * rows);
	if (!map->arr) {
		free(map);
		return NULL;
	}

	for (int i = 0; i < rows; i++) {
		map->arr[i] = malloc(sizeof(int) * columns);
		if (!map->arr[i]) {
			for (int j = 0; j < i; j++)
				free(map->arr[j]);
			free(map->arr);
			free(map);
			return NULL;
		}
		for (int j = 0; j < columns; j++) {
			map->arr[i][j] = input[i][j];
		}
	}

	return map;
}

#define HEIGHT 1000
#define WIDTH  1000

int worldMap3[HEIGHT][WIDTH];

void generateMap() {
    for (int y = 0; y < HEIGHT; y++) {
        for (int x = 0; x < WIDTH; x++) {
            // Make borders walls
            if (y == 0 || y == HEIGHT - 1 || x == 0 || x == WIDTH - 1) {
                worldMap3[y][x] = 1;
            } else {
                // Random path or wall (about 70% walls, 30% paths)
                worldMap3[y][x] = (rand() % 10 > 2) ? 1 : 0;
            }
        }
    }
}


